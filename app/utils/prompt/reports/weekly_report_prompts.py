# Weekly Report 브리핑 스크립트용 LLM 프롬프트 템플릿
WEEKLY_REPORT_SCRIPT_PROMPT = """
아래의 Weekly Report 마크다운을 보고 브리핑 스크립트를 생성해주세요.

[사용자 정보]
- 일자: {month}월 {week}주차
- 시각: {now_time}

[Weekly Report 마크다운]
{markdown}

스크립트 작성 규칙:
1. 시작은 반드시 "안녕하세요. {month}월 {week}주차 리포트를 브리핑해드리겠습니다."로 시작해주세요.
2. 주간 성과, 대화/관심사 요약, 인사이트, 다음주 할 일을 차례대로 설명해주세요.
3. 각 항목은 명확하게 구분하여 전달해주세요.
4. 대화체로 친근하게 작성해주세요.
5. 마지막은 "주간 리포트를 마치겠습니다. 좋은 하루 보내세요."로 마무리해주세요.

예시:
안녕하세요. {month}월 {week}주차 주간 리포트를 브리핑해드리겠습니다.

이번 주에는 {{성과1}}, {{성과2}} 등을 완료하셨네요. 
특히 {{중요성과}}가 눈에 띕니다.

대화 내용을 보니 {{관심사1}}, {{관심사2}}에 관심이 있으셨고,
인사이트 측면에서는 {{인사이트1}}에 대해 깊이 있게 다뤘네요.

다음 주에는 {{할일1}}, {{할일2}} 등이 예정되어 있습니다.
{{중요할일}}을 특히 신경 써서 준비하시면 좋을 것 같아요.

주간 리포트를 마치겠습니다. 좋은 하루 보내세요."""

